package JavaClasses.day17_WhileLoops;

public class WhileLoop_Practice2 {

    public static void main(String[] args) {

        for(int i =0; i<100; i++){
            System.out.print("*");
        }

        System.out.println();

        int i = 0;
        while (i<100){
            System.out.print("*");
            i++;
        }

    }

}
