package JavaClasses.day02_Variables;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("\tHello\tWorld");
        // this is paragraph quote \t

        System.out.println("\nhello\nworld");
        // this creates a new line \n


       // print: My favorite TV show is "Game of Thrones"
        System.out.println("My Favorite Tv show is \"Game of Thrones\"");


        System.out.println("\\");





    }

}
