package JavaClasses.day15_ForLoop;

public class ForLoop {

    public static void main(String[] args) {

         for (int i = 0; i <= 5; i++ ){
             System.out.println("Hello World");
         }

         for (int x= 1; x<11; x++ ){
             System.out.print(x+" ");
         }

        System.out.println();

         for (int z=1; z<11; z++ ){
             System.out.print(z*z+" ");
         }

    }

}
