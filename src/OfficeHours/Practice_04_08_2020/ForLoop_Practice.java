package OfficeHours.Practice_04_08_2020;

public class ForLoop_Practice {

    public static void main(String[] args) {

        for (int i=0; i<=5; i++){
            System.out.println("Hello world");
        }


        System.out.println("============================");


        for (int i=1; i<=5;){
            i++; //
            System.out.println(i); // i becomes 2/3/4/5 4 time exec
            // i++; if place it here 1/2/3/4/5 5 time exec
        }



    }
}
